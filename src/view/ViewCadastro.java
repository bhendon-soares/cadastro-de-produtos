    public void readJTable() {
        DefaultTableModel tblModel = (DefaultTableModel) tabelaProdutos.getModel();
        tblModel.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (Produto p : pdao.read()) {

            tblModel.addRow(new Object[]{
                p.getId(),
                p.getCodigo(),
                p.getStatus(),
                p.getData_cadastro(),
                p.getNome(),
                p.getDescricao(),
                p.getQtd_estoque(),
                p.getEstoque_minimo(),
                p.getEstoque_maximo(),
                p.getPreco_compra(),
                p.getPreco_venda(),
                p.getFator_lucro(),
                p.getNcm(),
                p.getCodigo_barras(),
            });
        }
    }

