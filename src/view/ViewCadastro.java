    private void atualizarFatorLucro() {
    try {
        // Obter os valores de compra e venda como números
        float precoCompra = Float.parseFloat(txtPrecoCompra.getText());
        float precoVenda = Float.parseFloat(txtPrecoVenda.getText());

        // Calcular o fator de lucro
        float fatorLucro = ((precoVenda - precoCompra) / precoCompra) * 100;
        
        // Exiba o fator de lucro formatado no campo txtLucro     
        String fatorLucroStr = String.valueOf(String.format("%.2f", fatorLucro));
        fatorLucroStr = fatorLucroStr.replace(",", ".");
        txtLucro.setText(fatorLucroStr);
        
    } catch (NumberFormatException ex) {
        txtLucro.setText("");
    }
}
    

